/**
 * Generates src/templates/html/pulse-works.js and src/templates/html/forge.js
 * from the extracted ZIP sources.
 *
 * Run once: node scripts/gen-html-templates.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT   = resolve(fileURLToPath(import.meta.url), '../../');
const SRC    = 'C:/Users/dpm/Downloads/New folder (5)/comp/Design Website Template (1)';
const OUT    = resolve(ROOT, 'src/templates/html');

mkdirSync(OUT, { recursive: true });

function read(path) {
  return readFileSync(path, 'utf8');
}

/**
 * Process one HTML file:
 *  1. Replace <link rel="stylesheet" href="styles.css"> with inlined <style>.
 *  2. Escape backticks and ${...} so the string is safe inside a JS template literal.
 */
function process(htmlPath, cssContent) {
  let html = read(htmlPath);
  html = html.replace(
    /<link\s+rel="stylesheet"\s+href="styles\.css">/g,
    `<style>\n${cssContent}\n</style>`,
  );
  // Escape for JS template literal
  html = html.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
  return html;
}

// ── Pulse Works ──────────────────────────────────────────────────────────────

const pulseCss = read(`${SRC}/pulse/styles.css`);

const pulsePages = [
  { title: 'Home',     path: '/',         file: 'index.html'    },
  { title: 'Shop',     path: '/shop',     file: 'shop.html'     },
  { title: 'Product',  path: '/product',  file: 'product.html'  },
  { title: 'Lookbook', path: '/lookbook', file: 'lookbook.html' },
  { title: 'About',    path: '/about',    file: 'about.html'    },
];

const pulsePageEntries = pulsePages
  .map(({ title, path, file }) => {
    const html = process(`${SRC}/pulse/${file}`, pulseCss);
    return `    {\n      title: ${JSON.stringify(title)},\n      path: ${JSON.stringify(path)},\n      html: \`${html}\`,\n    }`;
  })
  .join(',\n');

const pulseJs = `// Auto-generated by scripts/gen-html-templates.mjs — do not edit by hand.
export const pulseWorksTemplate = {
  id: "pulse-works",
  name: "Pulse Works",
  category: "Fashion",
  tagline: "Drop-based streetwear. No restocks, ever.",
  accent: "#ff3a17",
  surface: "#0e0d0c",
  motif: "html-dark",
  isHtmlTemplate: true,
  contentJson: {
    _source: "html-template",
    pages: [
${pulsePageEntries},
    ],
  },
};
`;

writeFileSync(resolve(OUT, 'pulse-works.js'), pulseJs, 'utf8');
console.log('✓ wrote src/templates/html/pulse-works.js');

